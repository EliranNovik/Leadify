CREATE TABLE IF NOT EXISTS public.client_portal_access (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  new_lead_id UUID NULL REFERENCES public.leads(id) ON DELETE CASCADE,
  legacy_lead_id BIGINT NULL REFERENCES public.leads_lead(id) ON DELETE CASCADE,
  password_hash TEXT NOT NULL,
  enabled BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_by TEXT NULL,
  CONSTRAINT client_portal_access_one_lead CHECK (
    (new_lead_id IS NOT NULL AND legacy_lead_id IS NULL)
    OR (new_lead_id IS NULL AND legacy_lead_id IS NOT NULL)
  )
);

CREATE UNIQUE INDEX IF NOT EXISTS client_portal_access_new_lead_id_unique
  ON public.client_portal_access (new_lead_id)
  WHERE new_lead_id IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS client_portal_access_legacy_lead_id_unique
  ON public.client_portal_access (legacy_lead_id)
  WHERE legacy_lead_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS public.client_portal_sessions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  session_token UUID NOT NULL DEFAULT gen_random_uuid() UNIQUE,
  contact_id BIGINT NOT NULL REFERENCES public.leads_contact(id) ON DELETE CASCADE,
  new_lead_id UUID NULL REFERENCES public.leads(id) ON DELETE CASCADE,
  legacy_lead_id BIGINT NULL REFERENCES public.leads_lead(id) ON DELETE CASCADE,
  expires_at TIMESTAMPTZ NOT NULL,
  last_seen_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT client_portal_sessions_one_lead CHECK (
    (new_lead_id IS NOT NULL AND legacy_lead_id IS NULL)
    OR (new_lead_id IS NULL AND legacy_lead_id IS NOT NULL)
  )
);

CREATE INDEX IF NOT EXISTS idx_client_portal_sessions_token
  ON public.client_portal_sessions (session_token);

CREATE INDEX IF NOT EXISTS idx_client_portal_sessions_expires
  ON public.client_portal_sessions (expires_at);

CREATE TABLE IF NOT EXISTS public.client_portal_meeting_requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  contact_id BIGINT NOT NULL REFERENCES public.leads_contact(id) ON DELETE CASCADE,
  new_lead_id UUID NULL REFERENCES public.leads(id) ON DELETE CASCADE,
  legacy_lead_id BIGINT NULL REFERENCES public.leads_lead(id) ON DELETE CASCADE,
  preferred_date DATE NOT NULL,
  preferred_time_range TEXT NULL,
  notes TEXT NULL,
  status TEXT NOT NULL DEFAULT 'pending'
    CHECK (status IN ('pending', 'confirmed', 'cancelled')),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT client_portal_meeting_requests_one_lead CHECK (
    (new_lead_id IS NOT NULL AND legacy_lead_id IS NULL)
    OR (new_lead_id IS NULL AND legacy_lead_id IS NOT NULL)
  )
);

CREATE INDEX IF NOT EXISTS idx_client_portal_meeting_requests_lead_pending
  ON public.client_portal_meeting_requests (new_lead_id, legacy_lead_id, status)
  WHERE status = 'pending';

