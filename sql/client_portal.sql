CREATE OR REPLACE FUNCTION public.portal_login(
  p_lead_ref TEXT,
  p_email TEXT,
  p_password TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_lead JSONB;
  v_access public.client_portal_access;
  v_contact_id BIGINT;
  v_session public.client_portal_sessions;
  v_new_lead_id UUID;
  v_legacy_lead_id BIGINT;
  v_contact JSONB;
BEGIN
  v_lead := public._portal_resolve_lead_ref(p_lead_ref);
  IF v_lead IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Invalid email or password');
  END IF;

  v_new_lead_id := (v_lead->>'new_lead_id')::UUID;
  v_legacy_lead_id := (v_lead->>'legacy_lead_id')::BIGINT;

  v_access := public._portal_access_for_lead(v_new_lead_id, v_legacy_lead_id);
  IF v_access IS NULL OR v_access.password_hash IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Invalid email or password');
  END IF;

  IF NOT public.portal_check_password(p_password, v_access.password_hash) THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Invalid email or password');
  END IF;

  v_contact_id := public._portal_contact_on_lead(p_email, v_new_lead_id, v_legacy_lead_id);
  IF v_contact_id IS NULL THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Invalid email or password');
  END IF;

  INSERT INTO public.client_portal_sessions (
    contact_id, new_lead_id, legacy_lead_id, expires_at
  ) VALUES (
    v_contact_id,
    v_new_lead_id,
    v_legacy_lead_id,
    NOW() + INTERVAL '7 days'
  )
  RETURNING * INTO v_session;

  SELECT jsonb_build_object(
    'id', lc.id,
    'name', lc.name,
    'email', lc.email
  ) INTO v_contact
  FROM public.leads_contact lc
  WHERE lc.id = v_contact_id;

  RETURN jsonb_build_object(
    'ok', true,
    'session_token', v_session.session_token,
    'lead_ref', COALESCE(v_lead->>'lead_number', p_lead_ref),
    'lead_summary', v_lead,
    'contact', v_contact
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_validate_session(p_token UUID)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_session public.client_portal_sessions;
  v_lead JSONB;
  v_contact JSONB;
BEGIN
  v_session := public._portal_session_row(p_token);
  IF v_session IS NULL THEN
    RETURN jsonb_build_object('ok', false);
  END IF;

  UPDATE public.client_portal_sessions
  SET last_seen_at = NOW()
  WHERE id = v_session.id;

  IF v_session.legacy_lead_id IS NOT NULL THEN
    SELECT public._portal_resolve_lead_ref(v_session.legacy_lead_id::TEXT) INTO v_lead;
  ELSE
    SELECT public._portal_resolve_lead_ref(v_session.new_lead_id::TEXT) INTO v_lead;
  END IF;

  SELECT jsonb_build_object(
    'id', lc.id,
    'name', lc.name,
    'email', lc.email
  ) INTO v_contact
  FROM public.leads_contact lc
  WHERE lc.id = v_session.contact_id;

  RETURN jsonb_build_object(
    'ok', true,
    'session_token', v_session.session_token,
    'lead_summary', v_lead,
    'contact', v_contact,
    'expires_at', v_session.expires_at
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_logout(p_token UUID)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  DELETE FROM public.client_portal_sessions WHERE session_token = p_token;
  RETURN TRUE;
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_get_case_summary(p_token UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_session public.client_portal_sessions;
  v_lead JSONB;
  v_expert_name TEXT;
  v_handler_name TEXT;
  v_scheduler_name TEXT;
  v_category TEXT;
BEGIN
  v_session := public._portal_session_row(p_token);
  IF v_session IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_session.legacy_lead_id IS NOT NULL THEN
    SELECT public._portal_resolve_lead_ref(v_session.legacy_lead_id::TEXT) INTO v_lead;

    SELECT
      public._portal_role_display(public._portal_parse_employee_id(ll.expert_id::TEXT), NULL),
      public._portal_role_display(public._portal_parse_employee_id(ll.case_handler_id::TEXT), NULL),
      public._portal_role_display(public._portal_parse_employee_id(ll.meeting_scheduler_id::TEXT), NULL),
      mc.name
    INTO v_expert_name, v_handler_name, v_scheduler_name, v_category
    FROM public.leads_lead ll
    LEFT JOIN public.misc_category mc ON mc.id::TEXT = NULLIF(TRIM(ll.category_id::TEXT), '')
    WHERE ll.id = v_session.legacy_lead_id;
  ELSE
    SELECT public._portal_resolve_lead_ref(v_session.new_lead_id::TEXT) INTO v_lead;

    SELECT
      public._portal_role_display(public._portal_parse_employee_id(l.expert), l.expert),
      public._portal_role_display(
        COALESCE(
          public._portal_parse_employee_id(l.case_handler_id::TEXT),
          public._portal_parse_employee_id(l.handler)
        ),
        l.handler
      ),
      public._portal_role_display(public._portal_parse_employee_id(l.scheduler), l.scheduler),
      COALESCE(mc.name, NULLIF(TRIM(l.category::TEXT), ''))
    INTO v_expert_name, v_handler_name, v_scheduler_name, v_category
    FROM public.leads l
    LEFT JOIN public.misc_category mc ON mc.id::TEXT = NULLIF(TRIM(l.category_id::TEXT), '')
    WHERE l.id = v_session.new_lead_id;
  END IF;

  RETURN jsonb_build_object(
    'lead', v_lead,
    'expert_name', v_expert_name,
    'handler_name', v_handler_name,
    'scheduler_name', v_scheduler_name,
    'category', v_category
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_get_sub_efforts(p_token UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_session public.client_portal_sessions;
  v_rows JSONB;
BEGIN
  v_session := public._portal_session_row(p_token);
  IF v_session IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_session.legacy_lead_id IS NOT NULL THEN
    SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.created_at DESC), '[]'::JSONB)
    INTO v_rows
    FROM (
      SELECT
        lse.id,
        lse.sub_effort_id,
        se.name AS sub_effort_name,
        lse.active,
        lse.client_notes,
        lse.document_url,
        lse.created_at,
        lse.updated_at,
        lse.updated_by
      FROM public.lead_sub_efforts lse
      INNER JOIN public.sub_efforts se ON se.id = lse.sub_effort_id
      WHERE lse.legacy_lead_id::TEXT = v_session.legacy_lead_id::TEXT
        AND lse.internal = FALSE
        AND lse.active = TRUE
      ORDER BY lse.created_at DESC
    ) t;
  ELSE
    SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.created_at DESC), '[]'::JSONB)
    INTO v_rows
    FROM (
      SELECT
        lse.id,
        lse.sub_effort_id,
        se.name AS sub_effort_name,
        lse.active,
        lse.client_notes,
        lse.document_url,
        lse.created_at,
        lse.updated_at,
        lse.updated_by
      FROM public.lead_sub_efforts lse
      INNER JOIN public.sub_efforts se ON se.id = lse.sub_effort_id
      WHERE lse.new_lead_id::TEXT = v_session.new_lead_id::TEXT
        AND lse.internal = FALSE
        AND lse.active = TRUE
      ORDER BY lse.created_at DESC
    ) t;
  END IF;

  RETURN jsonb_build_object('rows', v_rows);
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_get_finances(p_token UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_session public.client_portal_sessions;
  v_payments JSONB;
  v_proformas JSONB;
BEGIN
  v_session := public._portal_session_row(p_token);
  IF v_session IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_session.legacy_lead_id IS NOT NULL THEN
    SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.due_date NULLS LAST, t.id), '[]'::JSONB)
    INTO v_payments
    FROM (
      SELECT
        fpr.id,
        fpr.date AS due_date,
        fpr.value,
        fpr.vat_value AS value_vat,
        (fpr.actual_date IS NOT NULL) AS paid,
        fpr.actual_date AS paid_at,
        fpr.client_id AS plan_contact_id,
        ac.name AS currency,
        pl.secure_token,
        pl.status AS link_status,
        pl.expires_at AS link_expires_at,
        TRUE AS is_legacy,
        NULL::TEXT AS public_token,
        NULL::BIGINT AS proforma_id,
        fpr."order" AS "order"
      FROM public.finances_paymentplanrow fpr
      LEFT JOIN public.accounting_currencies ac ON ac.id::TEXT = NULLIF(TRIM(fpr.currency_id::TEXT), '')
      LEFT JOIN LATERAL (
        SELECT pl2.secure_token, pl2.status, pl2.expires_at
        FROM public.payment_links pl2
        WHERE pl2.payment_plan_id::TEXT = fpr.id::TEXT
          AND pl2.is_legacy_payment_plan = TRUE
        ORDER BY pl2.created_at DESC
        LIMIT 1
      ) pl ON TRUE
      WHERE fpr.lead_id::TEXT = v_session.legacy_lead_id::TEXT
        AND fpr.cancel_date IS NULL
        AND (
          fpr.actual_date IS NOT NULL
          OR COALESCE(fpr.ready_to_pay, FALSE) = TRUE
        )
    ) t;

    SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.paid_at DESC NULLS LAST, t.id DESC), '[]'::JSONB)
    INTO v_proformas
    FROM (
      SELECT
        pi.id,
        pi.public_token,
        pi.created_at,
        TRUE AS is_legacy,
        fpr.actual_date AS paid_at,
        fpr.value,
        fpr.vat_value AS value_vat,
        ac.name AS currency
      FROM public.proformainvoice pi
      INNER JOIN public.finances_paymentplanrow fpr
        ON fpr.id = pi.ppr_id
       AND fpr.lead_id::TEXT = v_session.legacy_lead_id::TEXT
       AND fpr.cancel_date IS NULL
       AND fpr.actual_date IS NOT NULL
      LEFT JOIN public.accounting_currencies ac ON ac.id::TEXT = NULLIF(TRIM(fpr.currency_id::TEXT), '')
      WHERE pi.lead_id::TEXT = v_session.legacy_lead_id::TEXT
        AND pi.public_token IS NOT NULL
    ) t;
  ELSE
    SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.due_date NULLS LAST, t.id), '[]'::JSONB)
    INTO v_payments
    FROM (
      SELECT
        pp.id,
        pp.due_date,
        pp.value,
        pp.value_vat,
        COALESCE(pp.paid, FALSE) AS paid,
        pp.paid_at,
        pp.client_id AS plan_contact_id,
        COALESCE(pp.currency, ac.name, '₪') AS currency,
        pl.secure_token,
        pl.status AS link_status,
        pl.expires_at AS link_expires_at,
        FALSE AS is_legacy,
        pp.public_token,
        NULL::BIGINT AS proforma_id,
        pp.payment_order AS "order"
      FROM public.payment_plans pp
      LEFT JOIN public.accounting_currencies ac ON ac.id::TEXT = NULLIF(TRIM(pp.currency_id::TEXT), '')
      LEFT JOIN LATERAL (
        SELECT pl2.secure_token, pl2.status, pl2.expires_at
        FROM public.payment_links pl2
        WHERE pl2.payment_plan_id::TEXT = pp.id::TEXT
        ORDER BY pl2.created_at DESC
        LIMIT 1
      ) pl ON TRUE
      WHERE pp.lead_id::TEXT = v_session.new_lead_id::TEXT
        AND pp.cancel_date IS NULL
        AND (
          COALESCE(pp.paid, FALSE) = TRUE
          OR COALESCE(pp.ready_to_pay, FALSE) = TRUE
        )
    ) t;

    v_proformas := '[]'::JSONB;
  END IF;

  RETURN jsonb_build_object(
    'payments', v_payments,
    'proformas', v_proformas,
    'is_legacy', v_session.legacy_lead_id IS NOT NULL
  );
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_get_documents(p_token UUID)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_session public.client_portal_sessions;
  v_lead_number TEXT;
  v_docs JSONB;
BEGIN
  v_session := public._portal_session_row(p_token);
  IF v_session IS NULL THEN
    RETURN NULL;
  END IF;

  IF v_session.legacy_lead_id IS NOT NULL THEN
    SELECT COALESCE(NULLIF(TRIM(ll.lead_number::TEXT), ''), ll.id::TEXT)
    INTO v_lead_number
    FROM public.leads_lead ll
    WHERE ll.id = v_session.legacy_lead_id;
  ELSE
    SELECT COALESCE(NULLIF(TRIM(l.lead_number::TEXT), ''), NULLIF(TRIM(l.manual_id::TEXT), ''), l.id::TEXT)
    INTO v_lead_number
    FROM public.leads l
    WHERE l.id = v_session.new_lead_id;
  END IF;

  SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.created_at DESC), '[]'::JSONB)
  INTO v_docs
