CREATE OR REPLACE FUNCTION public.portal_staff_set_password(
  p_lead_id TEXT,
  p_lead_type TEXT,
  p_password TEXT,
  p_enabled BOOLEAN DEFAULT TRUE
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_new_lead_id UUID;
  v_legacy_lead_id BIGINT;
  v_hash TEXT;
  v_row public.client_portal_access;
BEGIN
  IF p_password IS NULL OR length(trim(p_password)) < 6 THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Password must be at least 6 characters');
  END IF;

  v_hash := public.portal_hash_password(p_password);

  IF lower(coalesce(p_lead_type, '')) = 'legacy' OR p_lead_id ~ '^legacy_' OR p_lead_id ~ '^\d+$' THEN
    v_legacy_lead_id := regexp_replace(p_lead_id, '^legacy_', '')::BIGINT;
    UPDATE public.client_portal_access
    SET
      password_hash = v_hash,
      enabled = COALESCE(p_enabled, TRUE),
      updated_at = NOW(),
      updated_by = auth.uid()::TEXT
    WHERE legacy_lead_id = v_legacy_lead_id
    RETURNING * INTO v_row;

    IF NOT FOUND THEN
      INSERT INTO public.client_portal_access (legacy_lead_id, password_hash, enabled, updated_by)
      VALUES (v_legacy_lead_id, v_hash, COALESCE(p_enabled, TRUE), auth.uid()::TEXT)
      RETURNING * INTO v_row;
    END IF;
  ELSE
    v_new_lead_id := p_lead_id::UUID;
    UPDATE public.client_portal_access
    SET
      password_hash = v_hash,
      enabled = COALESCE(p_enabled, TRUE),
      updated_at = NOW(),
      updated_by = auth.uid()::TEXT
    WHERE new_lead_id = v_new_lead_id
    RETURNING * INTO v_row;

    IF NOT FOUND THEN
      INSERT INTO public.client_portal_access (new_lead_id, password_hash, enabled, updated_by)
      VALUES (v_new_lead_id, v_hash, COALESCE(p_enabled, TRUE), auth.uid()::TEXT)
      RETURNING * INTO v_row;
    END IF;
  END IF;

  RETURN jsonb_build_object('ok', true, 'enabled', v_row.enabled, 'updated_at', v_row.updated_at);
END;
$$;

CREATE OR REPLACE FUNCTION public.portal_staff_get_status(
  p_lead_id TEXT,
  p_lead_type TEXT
)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_row public.client_portal_access;
  v_lead_ref TEXT;
BEGIN
  IF lower(coalesce(p_lead_type, '')) = 'legacy' OR p_lead_id ~ '^legacy_' OR (p_lead_id ~ '^\d+$' AND length(p_lead_id) < 20) THEN
    SELECT * INTO v_row
    FROM public.client_portal_access
    WHERE legacy_lead_id = regexp_replace(p_lead_id, '^legacy_', '')::BIGINT
    LIMIT 1;

    SELECT COALESCE(NULLIF(TRIM(lead_number::TEXT), ''), id::TEXT)
    INTO v_lead_ref
    FROM public.leads_lead
    WHERE id = regexp_replace(p_lead_id, '^legacy_', '')::BIGINT;
  ELSE
    SELECT * INTO v_row
    FROM public.client_portal_access
    WHERE new_lead_id = p_lead_id::UUID
    LIMIT 1;

    SELECT COALESCE(NULLIF(TRIM(lead_number::TEXT), ''), NULLIF(TRIM(manual_id::TEXT), ''), id::TEXT)
    INTO v_lead_ref
    FROM public.leads
    WHERE id = p_lead_id::UUID;
  END IF;

  IF NOT FOUND OR v_row IS NULL THEN
    RETURN jsonb_build_object('enabled', true, 'has_password', false, 'lead_ref', v_lead_ref);
  END IF;

  RETURN jsonb_build_object(
    'enabled', v_row.enabled,
    'has_password', v_row.password_hash IS NOT NULL,
    'updated_at', v_row.updated_at,
    'lead_ref', v_lead_ref
  );
END;
$$;

