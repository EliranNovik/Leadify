-- -----------------------------------------------------------------------------
-- 2. POA DOCUMENTS (instances created per contact)
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.poa_documents (
  id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  secure_token  TEXT NOT NULL UNIQUE DEFAULT ('poa_' || replace(gen_random_uuid()::text, '-', '')),

  poa_type_id   BIGINT NOT NULL REFERENCES public.poa_types(id) ON DELETE RESTRICT,

  -- A POA always belongs to a contact, and to one of the two lead tables.
  contact_id    BIGINT NOT NULL REFERENCES public.leads_contact(id) ON DELETE CASCADE,
  new_lead_id   UUID NULL REFERENCES public.leads(id) ON DELETE SET NULL,
  legacy_lead_id BIGINT NULL REFERENCES public.leads_lead(id) ON DELETE SET NULL,

  status        TEXT NOT NULL DEFAULT 'pending'
                  CHECK (status IN ('pending', 'sent', 'viewed', 'signed', 'cancelled')),

  language      TEXT NULL,                         -- snapshot of the type language at creation

  -- Free-form, type-specific captured field values + base64 signature images.
  field_data    JSONB NOT NULL DEFAULT '{}'::jsonb,
  signatures    JSONB NOT NULL DEFAULT '{}'::jsonb,

  signer_name   TEXT NULL,
  signer_email  TEXT NULL,
  signed_pdf_path TEXT NULL,                        -- optional storage path of generated PDF

  created_by    TEXT NULL,                          -- staff identifier (auth uid / email)
  created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
  sent_at       TIMESTAMPTZ NULL,
  viewed_at     TIMESTAMPTZ NULL,
  signed_at     TIMESTAMPTZ NULL,
  expires_at    TIMESTAMPTZ NULL
);

COMMENT ON TABLE public.poa_documents IS 'Power of Attorney instances created for a lead contact; filled and signed on a public link.';
COMMENT ON COLUMN public.poa_documents.secure_token IS 'Public token used in /poa/:token link.';
COMMENT ON COLUMN public.poa_documents.field_data IS 'Type-specific field values captured from the public form.';
COMMENT ON COLUMN public.poa_documents.signatures IS 'Base64 signature images keyed by signature field id.';

CREATE INDEX IF NOT EXISTS idx_poa_documents_contact ON public.poa_documents(contact_id);
CREATE INDEX IF NOT EXISTS idx_poa_documents_new_lead ON public.poa_documents(new_lead_id);
CREATE INDEX IF NOT EXISTS idx_poa_documents_legacy_lead ON public.poa_documents(legacy_lead_id);
CREATE INDEX IF NOT EXISTS idx_poa_documents_status ON public.poa_documents(status);

-- keep updated_at fresh
CREATE OR REPLACE FUNCTION public._poa_touch_updated_at()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at := now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_poa_documents_touch ON public.poa_documents;
CREATE TRIGGER trg_poa_documents_touch
  BEFORE UPDATE ON public.poa_documents
  FOR EACH ROW EXECUTE FUNCTION public._poa_touch_updated_at();

-- -----------------------------------------------------------------------------
-- 3. RLS
-- -----------------------------------------------------------------------------
ALTER TABLE public.poa_types ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.poa_documents ENABLE ROW LEVEL SECURITY;

-- POA types are non-sensitive lookup data: readable by staff.
DROP POLICY IF EXISTS poa_types_select_auth ON public.poa_types;
CREATE POLICY poa_types_select_auth
  ON public.poa_types FOR SELECT TO authenticated
  USING (true);

-- POA documents: authenticated staff have full access; anon has NONE
-- (anon reaches data only through the SECURITY DEFINER RPCs below).
DROP POLICY IF EXISTS poa_documents_all_auth ON public.poa_documents;
CREATE POLICY poa_documents_all_auth
  ON public.poa_documents FOR ALL TO authenticated
  USING (true) WITH CHECK (true);

GRANT SELECT ON public.poa_types TO authenticated;
GRANT SELECT, INSERT, UPDATE, DELETE ON public.poa_documents TO authenticated;

-- =============================================================================
-- 4. PUBLIC RPCs (anon) — by secure_token only
-- =============================================================================

-- Fetch a POA for the public signing page. Marks it as viewed the first time.
CREATE OR REPLACE FUNCTION public.poa_get_public(p_token TEXT)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_poa public.poa_documents;
  v_type public.poa_types;
  v_contact public.leads_contact;
BEGIN
  SELECT * INTO v_poa FROM public.poa_documents WHERE secure_token = p_token;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'POA not found');
  END IF;

  IF v_poa.status = 'cancelled' THEN
    RETURN jsonb_build_object('ok', false, 'error', 'This power of attorney is no longer available.');
  END IF;

  IF v_poa.expires_at IS NOT NULL AND v_poa.expires_at < now() THEN
    RETURN jsonb_build_object('ok', false, 'error', 'This power of attorney link has expired.');
  END IF;

  SELECT * INTO v_type FROM public.poa_types WHERE id = v_poa.poa_type_id;
  SELECT * INTO v_contact FROM public.leads_contact WHERE id = v_poa.contact_id;

  -- First view: flip pending/sent -> viewed
  IF v_poa.status IN ('pending', 'sent') THEN
    UPDATE public.poa_documents
      SET status = 'viewed', viewed_at = COALESCE(viewed_at, now())
      WHERE id = v_poa.id;
    v_poa.status := 'viewed';
  END IF;

  RETURN jsonb_build_object(
    'ok', true,
    'poa', jsonb_build_object(
      'id', v_poa.id,
      'status', v_poa.status,
      'field_data', v_poa.field_data,
      'signatures', v_poa.signatures,
      'signer_name', v_poa.signer_name,
      'signer_email', v_poa.signer_email,
      'signed_at', v_poa.signed_at,
      'created_at', v_poa.created_at
    ),
    'type', jsonb_build_object(
      'id', v_type.id,
      'key', v_type.key,
      'name', v_type.name,
      'language', v_type.language,
      'direction', v_type.direction,
      'jurisdiction', v_type.jurisdiction,
      'description', v_type.description
    ),
    'contact', jsonb_build_object(
      'id', v_contact.id,
      'name', v_contact.name,
      'email', v_contact.email,
      'phone', v_contact.phone,
      'mobile', v_contact.mobile,
      'address', v_contact.address,
      'id_passport', v_contact.id_passport
    )
  );
END;
$$;

-- Submit (sign) a POA from the public page.
CREATE OR REPLACE FUNCTION public.poa_submit_public(
  p_token TEXT,
  p_field_data JSONB,
  p_signatures JSONB,
  p_signer_name TEXT DEFAULT NULL,
  p_signer_email TEXT DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_poa public.poa_documents;
BEGIN
  SELECT * INTO v_poa FROM public.poa_documents WHERE secure_token = p_token FOR UPDATE;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'POA not found');
  END IF;

  IF v_poa.status = 'cancelled' THEN
    RETURN jsonb_build_object('ok', false, 'error', 'This power of attorney is no longer available.');
  END IF;

  IF v_poa.status = 'signed' THEN
    RETURN jsonb_build_object('ok', false, 'error', 'This power of attorney was already signed.');
  END IF;

  IF v_poa.expires_at IS NOT NULL AND v_poa.expires_at < now() THEN
    RETURN jsonb_build_object('ok', false, 'error', 'This power of attorney link has expired.');
  END IF;

  UPDATE public.poa_documents
    SET field_data = COALESCE(p_field_data, '{}'::jsonb),
        signatures = COALESCE(p_signatures, '{}'::jsonb),
        signer_name = COALESCE(NULLIF(trim(p_signer_name), ''), signer_name),
        signer_email = COALESCE(NULLIF(trim(p_signer_email), ''), signer_email),
        status = 'signed',
        signed_at = now()
    WHERE id = v_poa.id;

  RETURN jsonb_build_object('ok', true, 'id', v_poa.id);
END;
$$;

GRANT EXECUTE ON FUNCTION public.poa_get_public(TEXT) TO anon, authenticated;
GRANT EXECUTE ON FUNCTION public.poa_submit_public(TEXT, JSONB, JSONB, TEXT, TEXT) TO anon, authenticated;

