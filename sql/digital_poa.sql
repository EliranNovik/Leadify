-- Create a POA for a contact and return the row (incl. secure_token).
CREATE OR REPLACE FUNCTION public.poa_create(
  p_contact_id BIGINT,
  p_poa_type_id BIGINT,
  p_new_lead_id UUID DEFAULT NULL,
  p_legacy_lead_id BIGINT DEFAULT NULL,
  p_prefill JSONB DEFAULT '{}'::jsonb,
  p_created_by TEXT DEFAULT NULL,
  p_expires_at TIMESTAMPTZ DEFAULT NULL
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_type public.poa_types;
  v_id UUID;
  v_token TEXT;
BEGIN
  SELECT * INTO v_type FROM public.poa_types WHERE id = p_poa_type_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Unknown POA type');
  END IF;

  IF NOT EXISTS (SELECT 1 FROM public.leads_contact WHERE id = p_contact_id) THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Contact not found');
  END IF;

  INSERT INTO public.poa_documents (
    poa_type_id, contact_id, new_lead_id, legacy_lead_id,
    language, field_data, status, created_by, expires_at
  )
  VALUES (
    p_poa_type_id, p_contact_id, p_new_lead_id, p_legacy_lead_id,
    v_type.language, COALESCE(p_prefill, '{}'::jsonb), 'pending', p_created_by, p_expires_at
  )
  RETURNING id, secure_token INTO v_id, v_token;

  RETURN jsonb_build_object(
    'ok', true,
    'id', v_id,
    'secure_token', v_token,
    'poa_type_id', p_poa_type_id,
    'type_key', v_type.key,
    'type_name', v_type.name
  );
END;
$$;

-- List POAs for a contact (newest first), with type info.
CREATE OR REPLACE FUNCTION public.poa_list_for_contact(p_contact_id BIGINT)
RETURNS JSONB
LANGUAGE plpgsql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_rows JSONB;
BEGIN
  SELECT COALESCE(jsonb_agg(row_to_json(t)::JSONB ORDER BY t.created_at DESC), '[]'::JSONB)
  INTO v_rows
  FROM (
    SELECT
      d.id,
      d.secure_token,
      d.status,
      d.poa_type_id,
      pt.key AS type_key,
      pt.name AS type_name,
      pt.language AS type_language,
      d.signer_name,
      d.created_at,
      d.sent_at,
      d.viewed_at,
      d.signed_at,
      d.expires_at
    FROM public.poa_documents d
    INNER JOIN public.poa_types pt ON pt.id = d.poa_type_id
    WHERE d.contact_id = p_contact_id
  ) t;

  RETURN jsonb_build_object('ok', true, 'poas', v_rows);
END;
$$;

-- Mark a POA as sent (e.g. after copying / sending the link).
CREATE OR REPLACE FUNCTION public.poa_mark_sent(p_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.poa_documents
    SET status = CASE WHEN status = 'pending' THEN 'sent' ELSE status END,
        sent_at = COALESCE(sent_at, now())
    WHERE id = p_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'POA not found');
  END IF;
  RETURN jsonb_build_object('ok', true);
END;
$$;

-- Cancel a POA (kept for audit; link stops working).
CREATE OR REPLACE FUNCTION public.poa_cancel(p_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  UPDATE public.poa_documents SET status = 'cancelled' WHERE id = p_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'POA not found');
  END IF;
  RETURN jsonb_build_object('ok', true);
END;
$$;

-- Hard-delete a POA (only when not signed).
CREATE OR REPLACE FUNCTION public.poa_delete(p_id UUID)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_status TEXT;
BEGIN
  SELECT status INTO v_status FROM public.poa_documents WHERE id = p_id;
  IF NOT FOUND THEN
    RETURN jsonb_build_object('ok', false, 'error', 'POA not found');
  END IF;
  IF v_status = 'signed' THEN
    RETURN jsonb_build_object('ok', false, 'error', 'Cannot delete a signed POA. Cancel it instead.');
  END IF;
  DELETE FROM public.poa_documents WHERE id = p_id;
  RETURN jsonb_build_object('ok', true);
END;
$$;

GRANT EXECUTE ON FUNCTION public.poa_create(BIGINT, BIGINT, UUID, BIGINT, JSONB, TEXT, TIMESTAMPTZ) TO authenticated;
GRANT EXECUTE ON FUNCTION public.poa_list_for_contact(BIGINT) TO authenticated;
GRANT EXECUTE ON FUNCTION public.poa_mark_sent(UUID) TO authenticated;
GRANT EXECUTE ON FUNCTION public.poa_cancel(UUID) TO authenticated;
GRANT EXECUTE ON FUNCTION public.poa_delete(UUID) TO authenticated;

-- Allow staff to read the POA type list for the "create POA" dropdown.
GRANT EXECUTE ON FUNCTION public.poa_get_public(TEXT) TO anon, authenticated;
