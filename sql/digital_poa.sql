-- =============================================================================
-- Digital Power of Attorney (POA) feature
-- -----------------------------------------------------------------------------
-- Public, token-based POA documents that clients fill out and sign online.
-- A contact (leads_contact) can have multiple POAs of different types.
-- POAs are linked to either a new lead (leads.id, UUID) or a legacy lead
-- (leads_lead.id, BIGINT), in addition to the contact.
--
-- Access model (mirrors client portal / public proforma):
--   * Tables have RLS enabled.
--   * Public (anon) access goes ONLY through SECURITY DEFINER RPCs by secure_token.
--   * Staff (authenticated) manage POAs via authenticated-only RPCs.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- 1. POA TYPES (templates / categories)
-- -----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.poa_types (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  key         TEXT NOT NULL UNIQUE,             -- machine key used by the frontend renderer
  name        TEXT NOT NULL,                    -- human-readable name (staff dropdown)
  language    TEXT NOT NULL DEFAULT 'en',       -- primary language: en / he / de
  direction   TEXT NOT NULL DEFAULT 'ltr',      -- ltr / rtl
  jurisdiction TEXT NULL,                       -- e.g. Germany / Austria / Israel
  description TEXT NULL,
  is_active   BOOLEAN NOT NULL DEFAULT TRUE,
  sort_order  INT NOT NULL DEFAULT 0,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE public.poa_types IS 'Power of Attorney template types (German citizenship, standard Hebrew, Austrian citizenship, etc.).';
COMMENT ON COLUMN public.poa_types.key IS 'Stable machine key matched by the frontend document renderer.';

